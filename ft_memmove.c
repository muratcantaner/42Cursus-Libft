#include "libft.h"

void	*ft_memmove(void *dst, const void *src, size_t len)
{	
	size_t				i;
	unsigned char		*dstt;
	unsigned char		*srcc;

	dstt = (unsigned char *)dst;
	srcc = (unsigned char *)src;
	i = 0;
	if (dst == src)
		return (dst);
	if (dst > src)
	{
		while (len--)
			*(dstt + len) = *(srcc + len);
		return (dst);
	}
	else
		return (ft_memcpy(dst, src, len));
}
